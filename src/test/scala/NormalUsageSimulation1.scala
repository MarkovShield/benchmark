			.get("/configuration/backend-apache/")
			.headers(headers_0))
		.pause(9)
		.exec(http("Req 5 CRIT /private/request-header/")
			.get("/private/request-header/")
			.headers(headers_0))
		.pause(3)
		.exec(http("Req 6 /login/login.php")
			.post("/login/login.php")
			.headers(headers_0)
			.formParam("user", "hacker")
